void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
  if (htim == &htim2 && htim->Channel == HAL_TIM_ACTIVE_CHANNEL_2) 
	{
		if (Is_First_Captured==0) 
		{
			/*char str[20];
			sprintf(str, "start %d\n\r", (int)HAL_GetTick());
			HAL_UART_Transmit(&huart1, (uint8_t*)str, strlen(str), 100);*/
			
			IC_Val1 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_2); 
			Is_First_Captured = 1;  
			__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_2, TIM_INPUTCHANNELPOLARITY_FALLING);
		}

		else if (Is_First_Captured==1)  
		{
			/*char str[20];
			sprintf(str, "stop %d\n\r", (int)HAL_GetTick());
			HAL_UART_Transmit(&huart1, (uint8_t*)str, strlen(str), 100);*/
			
			IC_Val2 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_2);
			__HAL_TIM_SET_COUNTER(htim, 0);  

			if (IC_Val2 > IC_Val1)
			{
				Difference = IC_Val2-IC_Val1;
			}

			else if (IC_Val1 > IC_Val2)
			{
				Difference = (0xffff - IC_Val1) + IC_Val2;
			}

			Distance = Difference * .034/2;
			Is_First_Captured = 0; 
			
			
			//Move Robot
			if(start == true && ADCVal > 0x400 && night == 1) {
			  HAL_GPIO_TogglePin(led3_GPIO_Port, led3_Pin); 
				
				recursive((int)Distance);
		  }
			else if(start == false || ADCVal <= 0x400 || night == 0) {
				Stop();
			}

			__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_2, TIM_INPUTCHANNELPOLARITY_RISING);
			__HAL_TIM_DISABLE_IT(&htim2, TIM_IT_CC2);
		}
	}
}

